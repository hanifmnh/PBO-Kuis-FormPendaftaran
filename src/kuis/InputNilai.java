package kuis;

public class InputNilai {
    int nik;
    String nama;
    int tulis;
    int coding;
    int wawancara;

    public InputNilai(int nik, String nama, int tulis, int coding, int wawancara) {
        this.nik = nik;
        this.nama = nama;
        this.tulis = tulis;
        this.coding = coding;
        this.wawancara = wawancara;
    }
}
